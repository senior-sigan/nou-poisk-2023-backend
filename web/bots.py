async def help_bot(db: Session, ws: WebSocket, username: str, text: str):
    messages = [
        "@ping return Pong",
        "@weather return precise weather in your location",
        "@dice return random number from x to y",
        "@goose return goose",
        "@flip to flip a coin",
        "@help return help for you)",
    ]
    for i in messages:
        await cm.broadcast(
            {
                "type": "message",
                "from": "@help",
                        "text": i,
            }
        )


